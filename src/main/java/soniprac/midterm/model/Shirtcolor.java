package soniprac.midterm.model;


public class Shirtcolor {

    String name;
    String length;
    String blue;
    String white;
    String yellow;
    String green;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {this.length = length;
    }

    public String isBlue() { return blue;}

    public void setBlue(String blue) {
        this.blue = blue;
    }

    public String isWhite() {
        return white;
    }

    public void setWhite(String white) {
        this.white = white;
    }

    public String isYellow() {
        return yellow;
    }

    public void setYellow(String yellow) {
        this.yellow = yellow;
    }

    public String isGreen() {
        return green;
    }

    public void setGreen(String green) {
        this.green = green;
    }
}
